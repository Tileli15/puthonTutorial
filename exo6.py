n1 = float(input("Enter the value of n1: "))
n2 = float(input("Enter the value of n2: "))
n3 = float(input("Enter the value of n3: "))
n4 = float(input("Enter the value of n4: "))
s = n1 + n2 + n3 + n4
m = s/4
print(f"The sum of {n1}, {n2}, {n3}, and {n4} is: {s}")
print(f"The average of {n1}, {n2}, {n3}, and {n4} is: {m}")